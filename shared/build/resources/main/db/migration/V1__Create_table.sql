CREATE TABLE groups (
  id BIGINT AUTO_INCREMENT,
  name VARCHAR(255) NOT NULL,
  created_ad TIMESTAMP NOT NULL,
  PRIMARY KEY(id)
) ENGINE = INNODB;

CREATE TABLE members (
  id BIGINT AUTO_INCREMENT,
  group_id BIGINT NOT NULL,
  name VARCHAR(255) NOT NULL,
  created_ad TIMESTAMP NOT NULL,
  PRIMARY KEY(id),
  FOREIGN KEY(group_id)
    REFERENCES groups(id) ON DELETE CASCADE
) ENGINE = INNODB;
